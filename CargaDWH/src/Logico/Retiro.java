
package scriptretiros;

/**
 *
 * @author John Medina
 */
public class Retiro {
    
    long fecha, cliente, demografia, planVoz, planDatos;

    public long getFecha() {
        return fecha;
    }

    public void setFecha(long fecha) {
        this.fecha = fecha;
    }

    public long getCliente() {
        return cliente;
    }

    public void setCliente(long cliente) {
        this.cliente = cliente;
    }

    public long getDemografia() {
        return demografia;
    }

    public void setDemografia(long demografia) {
        this.demografia = demografia;
    }

    public long getPlanVoz() {
        return planVoz;
    }

    public void setPlanVoz(long planVoz) {
        this.planVoz = planVoz;
    }

    public long getPlanDatos() {
        return planDatos;
    }

    public void setPlanDatos(long planDatos) {
        this.planDatos = planDatos;
    }

    
    
}
